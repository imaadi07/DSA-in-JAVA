import java.util.*;

public class LongestValdParentesis {
    public static void main(String[] args) {
        String s = "(()";
        int result = longestParenthesis(s);
        System.out.println(result);
    }

    public static int longestParenthesis(String s) {
        Stack<Integer> st = new Stack<>();
        int maxLen = 0;
        st.push(-1);

        for(int i=0; i<s.length(); i++) {
            char ch = s.charAt(i);

            if(ch == '(') {
                st.push(i);
            } else  { 
                st.pop(); 
                if(st.isEmpty()) {
                    st.push(i); 
                } else {
                    int len = i - st.peek();
                    maxLen = Math.max(maxLen, len);
                }
            }  
         }

        return maxLen;
    }
}
